package javaBuiltInMethods;

public class MathMethodsPractise {

	public static void main(String[] args) {

		
		System.out.println(Math.max(10, 50));
		
		System.out.println(Math.min(30, 15));
		
		System.out.println(Math.round(10.6));
		
		System.out.println(Math.round(10.3));
		
		System.out.println(Math.floor(10.5));
		
		System.out.println(Math.floor(9.6));
		
		System.out.println(Math.ceil(10.1));

	}

}
